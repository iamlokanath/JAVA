

public class typeCasting {
    public static void main(String[] args) {
        //Widening Casting
        int num = 45;
        double num1 = num;
        System.out.println(num);
        System.out.println(num1);
        double num3 = 80.90d;
        //Narrowing Casting
        int num4 = (int)num3;
        System.out.println(num4);
        System.out.println(num3);

    }
}
